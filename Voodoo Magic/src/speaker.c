unsigned char songsGetTotal() {
	return totalSongs;
}

void songsInitTitles() {
	for (int i = 0; i < totalSongs; i++) {
		char *title = malloc(sizeof(char) * 17);
		char *title_pos = title;
		char *song = songs[i];
		char *song_pos = song;
		for (int j = 0; j < strlen(song); j++) {
			if (*song_pos == ':') {
				*title_pos = NULL;
				break;
			}
			*title_pos = *song_pos;
			song_pos++;
			title_pos++;
		}
		titles[i] = title;
	}
}

char* songGet(unsigned char songNumber) {
	return songs[songNumber];
}

char* songGetTitle(unsigned char songNumber) {
	return titles[songNumber];
}
